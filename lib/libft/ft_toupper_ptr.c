void		ft_toupper_ptr(char *c)
{
	*c = (('a' <= *c && *c <= 'z') ? (*c) - 'a' + 'A' : *c);
}

void		ft_toupper_str(char *str)
{
	while (*str)
	{
		ft_toupper_ptr(str);
		str++;
	}
}
