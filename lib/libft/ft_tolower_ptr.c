void		ft_tolower_ptr(char *c)
{
	*c = (('A' <= *c && *c <= 'Z') ? (*c) - 'A' + 'a' : *c);
}

void		ft_tolower_str(char *str)
{
	while (*str)
	{
		ft_tolower_ptr(str);
		str++;
	}
}
